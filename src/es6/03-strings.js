let hello = 'Hello';
let world = 'World';
let epicPhrase = hello + ' ' + world + '!';
console.log(epicPhrase);

// Template literals
let epicPhrase2 = `${hello} ${world} 2!`;
console.log(epicPhrase2);

// Multiline strings
let lorem = 'esto es un string \n' + 'esto es otra línea';
console.log('lorem: ',lorem);

let lorem2 = `Esto es otra frase épica
y es multilínea`;
console.log('lorem2: ',lorem2);